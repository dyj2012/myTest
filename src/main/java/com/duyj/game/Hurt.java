package com.duyj.game;

/**
 * 伤害
 *
 * @author 杜永军
 * @date 2018/10/12
 */
public class Hurt {
    int baseHurt;
    int attachHurt;
    int reducedDefense;

    public int getBaseHurt() {
        return baseHurt;
    }

    public void setBaseHurt(int baseHurt) {
        this.baseHurt = baseHurt;
    }

    public int getAttachHurt() {
        return attachHurt;
    }

    public void setAttachHurt(int attachHurt) {
        this.attachHurt = attachHurt;
    }

    public int getReducedDefense() {
        return reducedDefense;
    }

    public void setReducedDefense(int reducedDefense) {
        this.reducedDefense = reducedDefense;
    }
}
